using System;

class FormatDemo
{
    static void Main()
    {
        // # Задание: Комментированная программа на C#
        // ## Демонстрация всех форматов Markdown в комментариях
        
        // **Ввод данных от пользователя**
        // Программа запрашивает *два числа* и выводит их ***сумму***
        
        Console.Write("Введите первое число: ");
        // `Convert.ToDouble()` преобразует строку в число
        double number1 = Convert.ToDouble(Console.ReadLine());
        
        Console.Write("Введите второе число: ");
        double number2 = Convert.ToDouble(Console.ReadLine());
        
        // ~~Вычисление разности~~ (устаревший код)
        // Актуально: **сложение** двух чисел
        double sum = number1 + number2;
        
        // ## Вывод результата
        // > Результат вычислений представлен ниже
        Console.WriteLine($"\n=== РЕЗУЛЬТАТ ===");
        Console.WriteLine($"Сумма чисел {number1} и {number2} = {sum}");
        
        // ### Дополнительная информация
        // - Первое число: **{number1}**
        // - Второе число: *{number2}*
        // - Результат: ***{sum}***
        
        /* 
        ## Блок комментария с Markdown
        Это многострочный комментарий, который тоже можно форматировать:
        - Пункт 1
        - Пункт 2
          - Подпункт 2.1
        
        `code` внутри комментария
        */
        
        // ## Конец программы
        Console.WriteLine("\nНажмите любую клавишу для выхода...");
        Console.ReadKey();
    }
}